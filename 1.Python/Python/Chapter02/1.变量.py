# 1.对“变量（variable）”的理解
# 添加一个名为message的变量（variable），它指向一个值（value）
# 理解一：变量是存储数据的盒子
# 理解二：变量是可以被赋值的标签，它指向特定的值
message = "This is a message"
print(message)

# 2.更新变量
# 在程序中，可随时修改变量的值，⽽ Python 将始终记录变量的最新值
message = "This is a new message"
print(message)

